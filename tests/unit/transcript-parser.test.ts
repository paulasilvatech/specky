describe("TranscriptParser — parseFile and format detection", () => {
  it("detects the format from the file extension", async () => {
    const { parser, workspace } = makeParser();
    writeFileSync(join(workspace, "meeting.vtt"), VTT_BASIC);
    writeFileSync(
      join(workspace, "meeting.srt"),
      "1\n00:00:01,000 --> 00:00:04,000\nAlice: Hi there.\n",
    );
    writeFileSync(join(workspace, "notes.md"), "**Alice:** Hello from markdown.");

    const vtt = await parser.parseFile("meeting.vtt");
    const srt = await parser.parseFile("meeting.srt");
    const md = await parser.parseFile("notes.md");

    expect(vtt.segments[0]).toMatchObject({
      speaker: "Alice",
      timestamp: expect.stringContaining("-->"),
    });
    expect(srt.segments[0]).toMatchObject({
      speaker: "Alice",
      timestamp: "00:00:01,000 --> 00:00:04,000",
    });
    expect(md.segments[0]).toEqual({ speaker: "Alice", text: "Hello from markdown." });
  });

  it("detects uppercase extensions case-insensitively", async () => {
    const { parser, workspace } = makeParser();
    writeFileSync(join(workspace, "MEETING.VTT"), VTT_BASIC);

    const analysis = await parser.parseFile("MEETING.VTT");

    expect(analysis.segments[0].timestamp).toBe("00:00:01.000 --> 00:00:04.000");
  });

  it("auto-detects VTT and SRT content inside .txt files", async () => {
    const { parser, workspace } = makeParser();
    writeFileSync(join(workspace, "vtt-content.txt"), VTT_BASIC);
    writeFileSync(
      join(workspace, "srt-content.txt"),
      "1\n00:00:01,000 --> 00:00:04,000\nAlice: Hi there.\n",
    );

    const vtt = await parser.parseFile("vtt-content.txt");
    const srt = await parser.parseFile("srt-content.txt");

    expect(vtt.segments[0].timestamp).toContain("-->");
    expect(srt.segments[0]).toMatchObject({ timestamp: "00:00:01,000 --> 00:00:04,000" });
  });

  it("falls back to plain text for unrecognized content", async () => {
    const { parser, workspace } = makeParser();
    writeFileSync(join(workspace, "plain.txt"), "Alice: plain text line.");

    const analysis = await parser.parseFile("plain.txt");

    expect(analysis.segments).toEqual([{ speaker: "Alice", text: "plain text line." }]);
  });

  it("rejects when the transcript file does not exist", async () => {
    const { parser } = makeParser();

    await expect(parser.parseFile("missing.vtt")).rejects.toThrow();
  });
});
