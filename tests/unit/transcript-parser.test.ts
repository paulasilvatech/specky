import { mkdtempSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { afterEach, describe, expect, it } from "vitest";
import { FileManager } from "../../src/services/file-manager.js";
import { TranscriptParser } from "../../src/services/transcript-parser.js";
import type { TranscriptAnalysis } from "../../src/types.js";

const workspaces: string[] = [];

afterEach(() => {
  for (const workspace of workspaces.splice(0)) {
    rmSync(workspace, { recursive: true, force: true, maxRetries: 5, retryDelay: 100 });
  }
});

function makeParser(): { parser: TranscriptParser; workspace: string } {
  const workspace = mkdtempSync(join(tmpdir(), "specky-transcript-parser-"));
  workspaces.push(workspace);
  return { parser: new TranscriptParser(new FileManager(workspace)), workspace };
}

const VTT_BASIC = `WEBVTT

NOTE
Auto-generated by the meeting recorder
and should be ignored

00:00:01.000 --> 00:00:04.000
<v Alice>Welcome to the quarterly review.</v>

00:00:05.000 --> 00:00:09.000
Bob: Thanks for joining today.

00:00:10.000 --> 00:00:15.000
<v Carol>We must review the <b>budget</b> numbers.</v>

00:00:20.000 --> 00:00:25.000
<v Dave:>Closing the call now.</v>
`;

describe("TranscriptParser — VTT format", () => {
  it("parses cues with <v Speaker> tags and Speaker: prefixes", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(VTT_BASIC, "vtt");

    expect(analysis.segments).toHaveLength(4);
    expect(analysis.segments[0]).toEqual({
      speaker: "Alice",
      text: "Welcome to the quarterly review.",
      timestamp: "00:00:01.000 --> 00:00:04.000",
    });
    expect(analysis.segments[1]).toMatchObject({
      speaker: "Bob",
      text: "Thanks for joining today.",
    });
    expect(analysis.segments[3]).toMatchObject({ speaker: "Dave:", text: "Closing the call now." });
  });

  it("removes the WEBVTT header and NOTE blocks from the content", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(VTT_BASIC, "vtt");

    expect(analysis.full_text).not.toContain("Auto-generated");
    expect(analysis.full_text).not.toContain("WEBVTT");
  });

  it("strips HTML tags from cue text", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(VTT_BASIC, "vtt");

    expect(analysis.segments[2].text).toBe("We must review the budget numbers.");
  });

  it("strips the trailing colon from <v Speaker:> participants", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(VTT_BASIC, "vtt");

    expect(analysis.participants).toEqual(["Alice", "Bob", "Carol", "Dave"]);
  });

  it("joins multi-line cue text and skips blocks without usable text", () => {
    const { parser } = makeParser();
    const vtt = `WEBVTT - recorder export

intro text before any cue

cue-17
00:01:00.000 --> 00:01:05.000
First line of the cue
second line of the cue

00:02:00.000 --> 00:02:05.000

00:03:00.000 --> 00:03:05.000
Plain text without a speaker tag
`;
    const analysis = parser.parse(vtt, "vtt");

    expect(analysis.segments).toHaveLength(2);
    expect(analysis.segments[0]).toMatchObject({
      speaker: "",
      text: "First line of the cue second line of the cue",
    });
    expect(analysis.segments[1]).toMatchObject({
      speaker: "",
      text: "Plain text without a speaker tag",
      timestamp: "00:03:00.000 --> 00:03:05.000",
    });
  });

  it("computes the duration from the first and last cue timestamps", () => {
    const { parser } = makeParser();
    const vtt = `WEBVTT

00:00:00.000 --> 00:00:04.000
Alice: Start of the meeting notes.

00:10:00.000 --> 00:10:05.000
Bob: Closing remarks for the meeting.
`;
    const analysis = parser.parse(vtt, "vtt");

    expect(analysis.duration_estimate).toBe("~10 minutes");
  });

  it("falls back to text-volume estimation when cue timestamps do not advance", () => {
    const { parser } = makeParser();
    const vtt = `WEBVTT

00:00:00.000 --> 00:00:00.500
Alice: one two three four five

00:00:00.500 --> 00:00:00.500
Bob: six seven eight nine ten
`;
    const analysis = parser.parse(vtt, "vtt");

    expect(analysis.duration_estimate).toBe("~1 minutes (estimated from text volume)");
  });
});

describe("TranscriptParser — SRT format", () => {
  const SRT = `1
00:00:01,000 --> 00:00:04,000
Alice: Welcome to the quarterly review.

2
00:00:05,000 --> 00:00:09,000
Bob: Thanks for <i>joining</i> the call today.

3
00:00:10,000 --> 00:00:15,000
No speaker attribution on this line
and a second line of the same cue.

4
00:00:40,000 --> 00:00:45,000
Carol: <b><i>Nested</i></b> formatting here.

5
00:00:20,000 --> 00:00:25,000
<b></b>

6

00:00:30,000 --> 00:00:35,000
Dangling timestamp block without an index line.
`;

  it("parses index/timestamp/text blocks with speaker prefixes", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(SRT, "srt");

    expect(analysis.segments[0]).toEqual({
      speaker: "Alice",
      text: "Welcome to the quarterly review.",
      timestamp: "00:00:01,000 --> 00:00:04,000",
    });
    expect(analysis.participants).toEqual(["Alice", "Bob", "Carol"]);
  });

  it("strips nested HTML tags and joins multi-line cue text", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(SRT, "srt");

    expect(analysis.segments[1].text).toBe("Thanks for joining the call today.");
    expect(analysis.segments[2]).toMatchObject({
      speaker: "",
      text: "No speaker attribution on this line and a second line of the same cue.",
    });
    expect(analysis.segments[3].text).toBe("Nested formatting here.");
  });

  it("skips blocks with fewer than three lines or with empty stripped text", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(SRT, "srt");

    expect(analysis.segments).toHaveLength(4);
    expect(analysis.full_text).not.toContain("Dangling");
  });

  it("falls back to text-volume estimation when timestamps are not parseable", () => {
    const { parser } = makeParser();
    const srt = `1
not-a-timestamp
Alice: some words here for the estimate

2
also-not-a-timestamp
Bob: more words here for the estimate
`;
    const analysis = parser.parse(srt, "srt");

    expect(analysis.duration_estimate).toContain("(estimated from text volume)");
  });
});

describe("TranscriptParser — plain text format", () => {
  it("splits capitalized Speaker: prefixes from unattributed lines", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: Welcome to the sync.",
        "bob: lowercase names are not detected as speakers.",
        "",
        "Just a floating line with no attribution.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.segments).toHaveLength(3);
    expect(analysis.segments[0]).toEqual({ speaker: "Alice", text: "Welcome to the sync." });
    expect(analysis.segments[1]).toEqual({
      speaker: "",
      text: "bob: lowercase names are not detected as speakers.",
    });
    expect(analysis.segments[2]).toEqual({
      speaker: "",
      text: "Just a floating line with no attribution.",
    });
    expect(analysis.participants).toEqual(["Alice"]);
  });

  it("defaults to the txt format when none is given", () => {
    const { parser } = makeParser();
    const analysis = parser.parse("Alice: hello there everyone");

    expect(analysis.segments).toEqual([{ speaker: "Alice", text: "hello there everyone" }]);
  });

  it("returns an empty analysis skeleton for empty content", () => {
    const { parser } = makeParser();
    const analysis = parser.parse("", "txt");

    expect(analysis.title).toBe("Meeting Transcript");
    expect(analysis.segments).toEqual([]);
    expect(analysis.participants).toEqual([]);
    expect(analysis.topics).toEqual([]);
    expect(analysis.decisions).toEqual([]);
    expect(analysis.action_items).toEqual([]);
    expect(analysis.full_text).toBe("");
    expect(analysis.duration_estimate).toBe("~1 minutes (estimated from text volume)");
  });
});

