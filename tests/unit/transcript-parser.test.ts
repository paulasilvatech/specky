describe("TranscriptParser — extraction heuristics", () => {
  it("clusters segments into named topics from keyword signals", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: We need login and oauth for authentication on the platform.",
        "Bob: The database must be postgres with encrypted storage.",
        "Alice: We decided the deployment will use docker containers in the pipeline.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.topics.map((topic) => topic.name)).toEqual([
      "Authentication & Authorization",
      "Data Storage",
      "Deployment & Infrastructure",
    ]);
    expect(analysis.topics[0]).toMatchObject({
      speakers: ["Alice"],
      key_points: [expect.any(String)],
    });
    expect(analysis.title).toBe("Authentication & Authorization");
  });

  it("keeps unmatched leading segments in a General Discussion topic", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: Welcome everyone and thanks for coming today.",
        "Bob: The api endpoints need webhook support soon.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.topics.map((topic) => topic.name)).toEqual([
      "General Discussion",
      "API Design",
    ]);
  });

  it("extracts deduplicated decisions across all phrasing patterns", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: We decided to use postgres for storage.",
        "Bob: Something else entirely here for context.",
        "Alice: We decided to use postgres for storage.",
        "Carol: The team approved the new api design.",
        "Dave: The cache layer will be redis for speed.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.decisions).toEqual([
      "[Alice] We decided to use postgres for storage.",
      "[Carol] The team approved the new api design.",
      "[Dave] The cache layer will be redis for speed.",
    ]);
  });

  it("extracts action items from speech signals", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: Bob needs to update the runbook by Friday.",
        "Bob: I will follow-up on the security review next week.",
        "Carol: This sentence has no action signals at all.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.action_items).toEqual([
      "[Alice] Bob needs to update the runbook by Friday.",
      "[Bob] I will follow-up on the security review next week.",
    ]);
  });

  it("extracts requirements with a minimum text length and dedupes them", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: The system shall support 500 concurrent users.",
        "Bob: Must go now.",
        "Carol: The platform needs to handle retries for failed jobs.",
        "Alice: The system shall support 500 concurrent users.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.requirements_raw).toEqual([
      "The system shall support 500 concurrent users.",
      "The platform needs to handle retries for failed jobs.",
    ]);
  });

  it("extracts constraints from budget, mandate, and compliance signals", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: The budget cannot exceed ten thousand dollars this quarter.",
        "Bob: We must use the existing identity provider for login.",
        "Carol: Everything is open for discussion here.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.constraints_mentioned).toEqual([
      "The budget cannot exceed ten thousand dollars this quarter.",
      "We must use the existing identity provider for login.",
    ]);
  });

  it("extracts questions by punctuation and question words", () => {
    const { parser } = makeParser();
    const analysis = parser.parse(
      [
        "Alice: How do we deploy this thing to production",
        "Bob: What is the rollback plan?",
        "Carol: Statements are not questions.",
      ].join("\n"),
      "txt",
    );

    expect(analysis.open_questions).toEqual([
      "[Alice] How do we deploy this thing to production",
      "[Bob] What is the rollback plan?",
    ]);
  });

  it("caps the open questions list at fifteen entries", () => {
    const { parser } = makeParser();
    const lines = Array.from(
      { length: 20 },
      (_, index) => `Alice: Unique question number ${index + 1} for the group?`,
    );
    const analysis = parser.parse(lines.join("\n"), "txt");

    expect(analysis.open_questions).toHaveLength(15);
  });

  it("derives the title from the first segment when no title or topics exist", () => {
    const { parser } = makeParser();
    const analysis = parser.parse('---\nauthor: "Release Bot"\n---\n', "md");

    expect(analysis.title).toBe("Release Bot");
    expect(analysis.participants).toEqual([]);
    expect(analysis.topics).toEqual([]);
  });

  it("skips comments and single-quoted values in YAML frontmatter", () => {
    const { parser } = makeParser();
    const analysis = parser.parse("---\n# Recorder metadata\ntitle: 'Single Quoted'\n---\n", "md");

    expect(analysis.title).toBe("Single Quoted");
  });

  it("estimates duration from word volume for untimed text", () => {
    const { parser } = makeParser();
    const words = Array.from({ length: 300 }, (_, index) => `token${index}`).join(" ");
    const analysis = parser.parse(words, "txt");

    expect(analysis.duration_estimate).toBe("~2 minutes (estimated from text volume)");
  });
});

